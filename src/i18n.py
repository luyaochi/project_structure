def get_text(key: str, lang: str = DEFAULT_LANG) -> str:
    """獲取翻譯文字"""
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG]).get(key, key)


def get_lang_suffix(lang: str) -> str:
    """獲取語言後綴"""
    if lang == LANG_ZH_TW:
        return ''  # 繁體中文為預設，不加後綴
    elif lang == LANG_EN:
        return '.en'
    elif lang == LANG_ZH_CN:
        return '.zh-CN'
    else:
        return ''
